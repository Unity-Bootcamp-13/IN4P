public class Stats
{
    public float hp;
    public float atk;
    public float atkSpeed;
    public float speed;
    public float atkRange;
    public float projectileSpeed;

    public Stats(float hp, float atk, float atkSpeed, float speed, float atkRange, float projectileSpeed)
    {
        this.hp = hp;
        this.atk = atk;
        this.atkSpeed = atkSpeed;
        this.speed = speed;
        this.atkRange = atkRange;
        this.projectileSpeed = projectileSpeed;
    }
}